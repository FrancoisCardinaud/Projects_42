#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!*s && !s)
		return (NULL);
	if (*s == (char)c)
		return ((char *)s);
	while (*s++)
	{
		if (*s == (char)c)
			return ((char *)s);
	}
	return (NULL);
}
