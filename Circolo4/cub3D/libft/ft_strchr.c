#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int				i;
	unsigned char	character;

	i = 0;
	character = c;
	if (character == '\0')
	{
		i = ft_strlen(str);
		return ((char *)str + i++);
	}
	while (str[i])
	{
		if (str[i] == character)
			return ((char *)str + i);
		i++;
	}
	return (NULL);
}
